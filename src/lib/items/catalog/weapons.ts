import type { WeaponCatalogItem, ItemRarity, AttachmentPoint } from "@/lib/items/types";

function asset(id: string) {
  return {
    iconPath: `/assets/items/weapons/icons/${id}.png`,
    inventoryPath: `/assets/items/weapons/icons/${id}.png`,
  };
}

function w(partial: {
  id: string;
  name: string;
  rarity: ItemRarity;
  weaponClass: WeaponCatalogItem["weaponClass"];
  affinity: WeaponCatalogItem["affinity"];
  description: string;
  attack: number;
  defense: number;
  speed: number;
  anatomy: string[];
  attachment: AttachmentPoint;
  affixes?: WeaponCatalogItem["affixes"];
  passive?: string;
  supply?: WeaponCatalogItem["supply"];
  totalSupply?: number;
}): WeaponCatalogItem {
  return {
    id: partial.id,
    name: partial.name,
    description: partial.description,
    family: "WEAPON",
    shopCategory: "WEAPONS",
    rarity: partial.rarity,
    affinity: partial.affinity,
    levelRequirement: partial.rarity === "COMMON" ? 1 : partial.rarity === "UNCOMMON" ? 3 : 5,
    tradeability: partial.rarity === "CELESTIAL" ? "ACCOUNT_BOUND" : "TRADEABLE",
    ownership: ["MYTHIC", "CELESTIAL"].includes(partial.rarity)
      ? "ON_CHAIN_OPTIONAL"
      : "OFF_CHAIN",
    stackable: false,
    maxStack: 1,
    priceKey: partial.id,
    supply: partial.supply ?? "UNLIMITED",
    totalSupply: partial.totalSupply,
    remainingSupply: partial.totalSupply,
    affixes: partial.affixes ?? [],
    ...asset(partial.id),
    compatibleAnatomy: partial.anatomy,
    attachment: partial.attachment,
    weaponClass: partial.weaponClass,
    stats: {
      attack: partial.attack,
      defense: partial.defense,
      speed: partial.speed,
    },
    passive: partial.passive,
  };
}

