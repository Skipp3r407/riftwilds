import type { EmoteDef } from "@/game/live-world/systems/emotes/types";

const icon = (key: string) => `/assets/game/emotes/icons/${key}.png`;
const preview = (key: string) => `/assets/game/emotes/previews/${key}.png`;
const audio = (key: string) => `/assets/game/emotes/audio/${key}.ogg`;

function def(
  partial: Omit<EmoteDef, "iconPath" | "previewPath" | "audioStub"> & {
    audioStub?: string;
  },
): EmoteDef {
  return {
    ...partial,
    iconPath: icon(partial.key),
    previewPath: preview(partial.key),
    audioStub: partial.audioStub ?? audio(partial.key),
  };
}

