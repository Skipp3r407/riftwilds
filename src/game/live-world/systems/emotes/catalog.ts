const byKey = new Map(EMOTE_CATALOG.map((e) => [e.key, e]));
const byAlias = new Map<string, EmoteDef>();
for (const e of EMOTE_CATALOG) {
  byAlias.set(e.key, e);
  for (const a of e.aliases ?? []) byAlias.set(a.toLowerCase(), e);
}

export function getEmoteDef(key: string): EmoteDef | undefined {
  return byKey.get(key) ?? byAlias.get(key.toLowerCase());
}

export function resolveEmoteKey(raw: string): string | null {
  const def = getEmoteDef(raw.trim().toLowerCase().replace(/\s+/g, "_"));
  return def?.key ?? null;
}

export function listEmotes(filter?: {
  kind?: EmoteDef["kind"];
  tier?: EmoteDef["tier"];
  starterOnly?: boolean;
}): EmoteDef[] {
  return EMOTE_CATALOG.filter((e) => {
    if (filter?.kind && e.kind !== filter.kind) return false;
    if (filter?.tier && e.tier !== filter.tier) return false;
    if (filter?.starterOnly && !STARTER_EMOTE_KEYS.includes(e.key as (typeof STARTER_EMOTE_KEYS)[number])) {
      return false;
    }
    return true;
  });
}

export function defaultWheelSlots(): (string | null)[] {
  return ["wave", "hello", "nod", "cheer", "dance", "ready", "thanks", "salute"];
}

/** Legacy stub shape for social hub. */
export function emoteCatalogForSocialStub() {
  return EMOTE_CATALOG.filter((e) => e.kind === "solo").slice(0, 8).map((e) => ({
    key: e.key,
    label: e.label,
    animationKey: e.animationKey,
    unlockHint: e.unlockHint,
  }));
}
