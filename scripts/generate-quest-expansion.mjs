function fmtObj([key, description, metric, target]) {
  return `      { key: ${JSON.stringify(key)}, description: ${JSON.stringify(description)}, metric: ${JSON.stringify(metric)}, target: ${target} }`;
}

function fmtReward(r) {
  if (r[0] === "xp") return `      { kind: "xp", amount: ${r[1]} }`;
  if (r[0] === "ap") return `      { kind: "arena_points", amount: ${r[1]} }`;
  return `      { kind: "care_item", itemKey: ${JSON.stringify(r[1])}, label: ${JSON.stringify(r[2])}, quantity: ${r[3]} }`;
}

function fmtQuest(q, sortOrder) {
  const lines = [
    "  {",
    `    key: ${JSON.stringify(q.key)},`,
    `    name: ${JSON.stringify(q.name)},`,
    `    description: ${JSON.stringify(q.description)},`,
    `    category: ${JSON.stringify(q.category)},`,
    `    boardTab: ${JSON.stringify(q.boardTab)},`,
    `    difficulty: ${JSON.stringify(q.difficulty)},`,
  ];
  if (q.region) {
    lines.push(`    regionKey: ${JSON.stringify(q.region.key)},`);
    lines.push(`    regionName: ${JSON.stringify(q.region.name)},`);
  }
  if (q.chainKey) lines.push(`    chainKey: ${JSON.stringify(q.chainKey)},`);
  lines.push(`    repeatable: ${q.repeatable === true},`);
  if (q.requires?.length) {
    lines.push(`    requires: ${JSON.stringify(q.requires)},`);
  }
  lines.push("    objectives: [");
  lines.push(q.objectives.map(fmtObj).join(",\n"));
  lines.push("    ],");
  lines.push("    rewards: [");
  lines.push(q.rewards.map(fmtReward).join(",\n"));
  lines.push("    ],");
  lines.push(`    sortOrder: ${sortOrder},`);
  lines.push("  }");
  return lines.join("\n");
}

