function buildAmbient(regionId, slug, name, title, col, row, behavior, kind = "ambient", extra = {}) {
  const coordinates = { x: col * TILE, y: row * TILE };
  const n = baseNpc({
    slug,
    displayName: name,
    shortName: name.split(" ")[0],
    title,
    kind,
    regionId,
    locationId: "ambient",
    coordinates,
    occupation: title,
    personalityTraits: ["ambient", "alive"],
    visualDescription: extra.visualDescription ?? `${name}, everyday Commons resident with distinct clothing colors`,
    clothing: extra.clothing ?? "Practical regional daywear",
    biography: `${name} lives and works in ${regionId.replace(/-/g, " ")}.`,
    greetingDialogue: [`Hey there — I'm ${name}. Just passing the day.`, `Need directions? The plaza Riftstone is the heart of Commons.`],
    ambientBehavior: behavior,
    interactionRadius: 48,
    ...extra,
  });
  n.dialogueNodes = defaultNodes(n);
  return n;
}

function buildNamed(spec) {
  const n = baseNpc(spec);
  if (!n.dialogueNodes?.length) n.dialogueNodes = defaultNodes(n);
  // Ensure greeting node has accept for primary quests
  return n;
}

