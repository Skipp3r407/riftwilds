const STYLE =
  "Premium stylized fantasy adventure, Riftwilds original IP, detailed but readable at game scale, strong silhouette, no text, no watermark, no logo, not anime franchise, not Pokémon, not Zelda, not WoW";

function assetPaths(region, slug) {
  const base = `/assets/npcs/${region}/${slug}`;
  return {
    portraitAsset: `${base}/portrait.png`,
    fullBodyAsset: `${base}/full-body.png`,
    spriteAsset: `${base}/sprite.png`,
    thumbnailAsset: `${base}/thumbnail.png`,
  };
}

function prompts(name, visual, clothing, regionLabel) {
  const base = `${STYLE}. Character: ${name}. ${visual}. Wearing: ${clothing}. Region vibe: ${regionLabel}.`;
  return {
    portrait: `${base} Bust portrait, shoulders up, clear face, soft rim light, plain dark gradient background.`,
    fullBody: `${base} Full body standing pose, feet visible, transparent or simple studio background, front three-quarter view.`,
    thumbnail: `${base} Tight head-and-shoulders icon crop, high contrast, centered face.`,
    sprite: `${base} Simple browser-game character sprite concept, front-facing idle stance, clean edges, solid readable colors, small scale friendly.`,
  };
}

function baseNpc(partial) {
  const slug = partial.slug;
  const region = partial.regionId;
  const assets = assetPaths(region, slug);
  const regionLabel = region.replace(/-/g, " ");
  return {
    id: slug,
    slug,
    shortName: partial.shortName ?? partial.displayName.split(" ").pop(),
    kind: partial.kind ?? "named",
    locationId: partial.locationId ?? "hub",
    spawnConditions: partial.spawnConditions ?? ["always"],
    schedule: partial.schedule ?? {
      presentDuring: ["dawn", "day", "dusk", "night"],
      homeX: partial.coordinates.x,
      homeY: partial.coordinates.y,
      wanderRadius: 24,
    },
    faction: partial.faction ?? "commons-keepers",
    species: partial.species ?? "Human (Riftwild-born)",
    ageRange: partial.ageRange ?? "adult",
    pronouns: partial.pronouns ?? "they/them",
    personalityTraits: partial.personalityTraits ?? ["steady"],
    biography: partial.biography,
    visualDescription: partial.visualDescription,
    clothing: partial.clothing,
    accessories: partial.accessories ?? "none notable",
    colorPalette: partial.colorPalette ?? ["#3de7ff", "#1a2438", "#e8e0ff"],
    dialogueStyle: partial.dialogueStyle ?? "plain and helpful",
    personalHistory: partial.personalHistory ?? partial.biography,
    riftlingRelationship: partial.riftlingRelationship ?? "Respects Riftlings as living world-fragments.",
    questFunction: partial.questFunction ?? "Regional guide",
    shopOrService: partial.shopOrService ?? "none",
    greetingDialogue: partial.greetingDialogue,
    repeatDialogue: partial.repeatDialogue ?? [
      "Still walking the paths? Good. The Commons needs keepers who return.",
    ],
    questDialogue: partial.questDialogue ?? ["There is work if you are ready."],
    shopDialogue: partial.shopDialogue ?? ["Browse what I can spare."],
    relationshipDialogue: partial.relationshipDialogue ?? ["Your Riftling trusts you — that matters."],
    dialogueNodes: partial.dialogueNodes ?? [],
    storyFlags: partial.storyFlags ?? [],
    questIds: partial.questIds ?? [],
    shopId: partial.shopId,
    serviceIds: partial.serviceIds ?? [],
    reputationRequirements: partial.reputationRequirements ?? {},
    animationSet: partial.animationSet ?? "idle_basic",
    ...assets,
    voiceStyle: partial.voiceStyle ?? "warm mid",
    ambientBehavior: partial.ambientBehavior ?? "idle",
    interactionRadius: partial.interactionRadius ?? 56,
    facingBehavior: partial.facingBehavior ?? "face_player",
    active: true,
    artStatus: "placeholder",
    imagePrompts: prompts(
      partial.displayName,
      partial.visualDescription,
      partial.clothing,
      regionLabel,
    ),
    createdAt: NOW,
    updatedAt: NOW,
    ...partial,
    ...assets,
  };
}

function defaultNodes(npc) {
  const nodes = [
    {
      id: "greeting",
      lines: npc.greetingDialogue,
      choices: [
        { id: "chat", label: "Tell me more", next: "lore" },
        ...(npc.questIds?.length
          ? [{ id: "quests", label: "About quests", next: "quest" }]
          : []),
        ...(npc.shopId
          ? [{ id: "shop", label: "Open shop", action: "open_shop", shopId: npc.shopId }]
          : []),
        ...(npc.serviceIds?.length
          ? [
              {
                id: "service",
                label: "Ask for help",
                action: "open_service",
                serviceId: npc.serviceIds[0],
              },
            ]
          : []),
        { id: "bye", label: "Goodbye", action: "close" },
      ],
    },
    {
      id: "lore",
      lines: npc.repeatDialogue,
      choices: [{ id: "back", label: "Back", next: "greeting" }],
    },
    {
      id: "quest",
      lines: npc.questDialogue,
      choices: [
        ...(npc.questIds?.[0]
          ? [
              {
                id: "accept",
                label: "Accept quest",
                action: "accept_quest",
                questId: npc.questIds[0],
              },
            ]
          : []),
        { id: "back", label: "Back", next: "greeting" },
      ],
    },
  ];
  return nodes;
}

